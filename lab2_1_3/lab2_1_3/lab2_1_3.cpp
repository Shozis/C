#include <iostream>
#include <cmath>
using namespace std;

int main()
{
	setlocale(LC_ALL, "Russian");

	double y;

	//for

	for (double x = -4; x <= 4; x += 0.5)
	{
		if (x <= 0) {

			y = pow(x, 2);
		}
		else if (0 < x && x <= 1.5) {

			y = cos(x);
		}
		else if (x > 1.5) {

			y = sin(x - 1);

			cout << y << endl;
		}
	}

	system("pause");
}

